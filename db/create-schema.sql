-- =====================================
-- PRODUCT SALE (PROMOTION)
-- =====================================

CREATE TABLE product_sale (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT NOT NULL,
    sale_id BIGINT NOT NULL,
    discount_value INT NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,

    CONSTRAINT fk_product_sale_product
        FOREIGN KEY (product_id)
        REFERENCES products(id)
        ON DELETE CASCADE,

    CONSTRAINT fk_product_sale_sale
        FOREIGN KEY (sale_id)
        REFERENCES sales(id)
        ON DELETE CASCADE,

    CONSTRAINT uq_product_sale UNIQUE(product_id, sale_id)
);

CREATE INDEX idx_product_sale_product ON product_sale(product_id);
CREATE INDEX idx_product_sale_sale ON product_sale(sale_id);